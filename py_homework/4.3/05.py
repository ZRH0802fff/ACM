x = [0.5, -1.2]
w1 = [[1, 1], [-1, 2], [0, -1]]
b1 = [0.1, 0.2, 0.3]
w2 = [[1, -1, 1]]
b2 = [0.5]

def f(z):
    return [max(0, v) for v in z]

z1 = [w1[0][0]*x[0] + w1[0][1]*x[1] + b1[0], 
      w1[1][0]*x[0] + w1[1][1]*x[1] + b1[1], 
      w1[2][0]*x[0] + w1[2][1]*x[1] + b1[2]]

h = f(z1)

y = [w2[0][0]*h[0] + w2[0][1]*h[1] + w2[0][2]*h[2] + b2[0]]

print(y)